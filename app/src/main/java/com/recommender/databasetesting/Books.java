package com.recommender.databasetesting;

public class Books {

    private String Name;
    private String Author;

    public Books() {

    }

    public String getAuthor() {
        return Author;
    }

    public void setAuthor(String author) {
        Author = author;
    }

    public String getName() {

        return Name;
    }

    public void setName(String name) {

        Name = name;
    }

}

