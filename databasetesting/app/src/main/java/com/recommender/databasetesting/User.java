package com.recommender.databasetesting;

public class User {

    private String Name;
    private String ID;

    public User() {
    }

    public String getID() {

        return ID;
    }

    public void setID(String ID) {

        this.ID = ID;
    }


    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }


}