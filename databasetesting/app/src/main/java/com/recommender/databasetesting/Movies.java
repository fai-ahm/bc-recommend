package com.recommender.databasetesting;

public class Movies {

        private String Name;
        private String Year;

        public Movies() {

        }

        public String getYear() {
            return Year;
        }
        public void setYear(String year) {
            Year = year;
        }

        public String getName() {

            return Name;
        }

        public void setName(String name) {

            Name = name;
        }

}
